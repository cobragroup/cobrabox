#!/usr/bin/env python3
"""Generate feature stubs for IDE/type-checker support.

Scans every .py file in the features package (excluding __init__.py) using AST,
finds all classes that inherit directly from BaseFeature, SplitterFeature, or
AggregatorFeature (with or without type-parameter subscript), and writes two
stub files with explicit re-exports so IDEs and type checkers can resolve them:

  src/cobrabox/features/__init__.pyi  — for `from cobrabox.features import X`
  src/cobrabox/feature.pyi            — for `cb.feature.X`

Exit codes:
  0 — stubs are already up to date
  1 — one or more stubs were updated (pre-commit will re-stage the files)
"""

from __future__ import annotations

import ast
import sys
from pathlib import Path

BASE_CLASSES = {"BaseFeature", "SplitterFeature", "AggregatorFeature"}

REPO_ROOT = Path(__file__).parent.parent
FEATURES_DIR = REPO_ROOT / "src" / "cobrabox" / "features"
FEATURES_STUB_PATH = FEATURES_DIR / "__init__.pyi"
FEATURE_STUB_PATH = REPO_ROOT / "src" / "cobrabox" / "feature.pyi"

HEADER = """\
# This file is auto-generated by scripts/gen_stubs.py — do not edit manually.
# Run `uv run python scripts/gen_stubs.py` to regenerate.
"""


def _base_name(node: ast.expr) -> str | None:
    """Return the base-class name from a Name or Subscript node."""
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.Subscript) and isinstance(node.value, ast.Name):
        return node.value.id
    return None


def find_feature_classes(path: Path) -> list[str]:
    """Return sorted list of feature class names defined in *path*."""
    tree = ast.parse(path.read_text(encoding="utf-8"))
    names = []
    for node in ast.walk(tree):
        if isinstance(node, ast.ClassDef):
            bases = {_base_name(b) for b in node.bases}
            if bases & BASE_CLASSES:
                names.append(node.name)
    return sorted(names)


def _collect_classes() -> dict[str, list[str]]:
    """Return {module_stem: [ClassName, ...]} for all feature modules."""
    result: dict[str, list[str]] = {}
    for module_path in sorted(p for p in FEATURES_DIR.glob("*.py") if p.name != "__init__.py"):
        classes = find_feature_classes(module_path)
        if classes:
            result[module_path.stem] = classes
    return result


def _all_block(names: list[str]) -> str:
    items = "".join(f'    "{n}",\n' for n in sorted(names))
    return f"\n__all__ = [\n{items}]\n"


def generate_features_stub(classes_by_module: dict[str, list[str]]) -> str:
    """Stub for cobrabox/features/__init__.pyi."""
    lines: list[str] = [HEADER]
    all_names: list[str] = []
    for stem, classes in classes_by_module.items():
        for cls in classes:
            lines.append(f"from .{stem} import {cls} as {cls}\n")
            all_names.append(cls)
    lines.append(_all_block(all_names))
    return "".join(lines)


def generate_feature_stub(classes_by_module: dict[str, list[str]]) -> str:
    """Stub for cobrabox/feature.pyi (the cb.feature namespace)."""
    lines: list[str] = [HEADER]
    all_names: list[str] = []
    for classes in classes_by_module.values():
        for cls in classes:
            lines.append(f"from .features import {cls} as {cls}\n")
            all_names.append(cls)
    lines.append(_all_block(all_names))
    return "".join(lines)


def _write_if_changed(path: Path, content: str) -> bool:
    current = path.read_text(encoding="utf-8") if path.exists() else ""
    if content == current:
        return False
    path.write_text(content, encoding="utf-8")
    print(f"Updated {path.relative_to(REPO_ROOT)}", file=sys.stderr)
    return True


def main() -> int:
    classes_by_module = _collect_classes()
    changed = False
    changed |= _write_if_changed(FEATURES_STUB_PATH, generate_features_stub(classes_by_module))
    changed |= _write_if_changed(FEATURE_STUB_PATH, generate_feature_stub(classes_by_module))
    return 1 if changed else 0


if __name__ == "__main__":
    sys.exit(main())
